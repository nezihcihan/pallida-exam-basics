package takemetothejava;

public class Lollipops extends Sweets{


    public Lollipops () {
        this.price = 10;
        this.sugar = 5;
    }

    public float getPrice() {
        return price;
    }

    public float getSugar() {
        return sugar;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setSugar(float sugar) {
        this.sugar = sugar;
    }

}
